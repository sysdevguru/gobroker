// more convenient than defining an entire order
type ord struct {
	side    enum.Side
	symbol  *string
	tOffset *time.Duration
}

// defining a trade sequence to make assertions against
type tradeSeq struct {
	filled   []ord
	pending  []ord
	incoming *ord
}

// just a couple of shortcuts, who wants to type ord{side: enum.Buy} all day?
func buy() ord {
	return ord{side: enum.Buy}
}

func sell() ord {
	return ord{side: enum.Sell}
}

func (s *AccountSuite) TestPatternDayTrades() {
	paca := "PACA"
	yesterday := -24 * time.Hour
	old := -10 * 24 * time.Hour

	// no trades
	s.assertPDT(tradeSeq{}, 0)

	// first trade is a sell
	s.assertPDT(tradeSeq{
		incoming: &ord{side: enum.Sell},
	}, 0)

	// first trade is a buy
	s.assertPDT(tradeSeq{
		incoming: &ord{side: enum.Sell},
	}, 0)

	// buy then sell
	s.assertPDT(tradeSeq{
		filled: []ord{
			buy(),
		},
		incoming: &ord{side: enum.Sell}, //dt
	}, 1)

	// sell then buy
	s.assertPDT(tradeSeq{
		filled: []ord{
			sell(),
		},
		incoming: &ord{side: enum.Buy},
	}, 0)

	s.assertPDT(tradeSeq{
		filled: []ord{
			buy(),
			sell(), //dt
			sell(),
			sell(),
			buy(),
		},
		incoming: &ord{side: enum.Sell}, //dt
	}, 2)

	s.assertPDT(tradeSeq{
		filled: []ord{
			sell(),
			sell(),
			sell(),
			buy(),
			buy(),
			buy(),
		},
		incoming: &ord{side: enum.Sell}, //dt
	}, 1)

	s.assertPDT(tradeSeq{
		pending: []ord{
			buy(),
			buy(),
		},
		incoming: &ord{side: enum.Sell}, //dt
	}, 1)

	// several sells after a buy
	s.assertPDT(tradeSeq{
		filled: []ord{
			buy(),
		},
		pending: []ord{
			sell(),
			sell(),
		},
		incoming: &ord{side: enum.Sell},
	}, 1)

	s.assertPDT(tradeSeq{
		filled: []ord{
			buy(),
			sell(), //dt
			buy(),
			sell(), //dt
			buy(),
			sell(), //dt
			buy(),
		},
		incoming: &ord{
			side: enum.Buy,
		},
	}, 3)

	s.assertPDT(tradeSeq{
		pending: []ord{
			buy(),
			sell(),
			buy(),
			sell(),
			buy(),
			sell(),
			buy(),
		},
		incoming: &ord{side: enum.Sell}, //dt
	}, 4)

	s.assertPDT(tradeSeq{
		filled: []ord{
			ord{symbol: &paca, side: enum.Buy},
			ord{symbol: &paca, side: enum.Sell}, //dt
			ord{symbol: &paca, side: enum.Buy},
			ord{symbol: &paca, side: enum.Sell}, //dt
			ord{symbol: &paca, side: enum.Buy},
			ord{symbol: &paca, side: enum.Sell}, //dt
			ord{symbol: &paca, side: enum.Buy},
		},
		incoming: &ord{
			side: enum.Sell, // different symbol, not a dt
		},
	}, 3)

	s.assertPDT(tradeSeq{
		filled: []ord{
			ord{symbol: &paca, side: enum.Buy},
			sell(),
			ord{symbol: &paca, side: enum.Buy},
			sell(),
			ord{symbol: &paca, side: enum.Sell}, //dt
			buy(),
			sell(), // dt
		},
	}, 2)

	s.assertPDT(tradeSeq{
		pending: []ord{
			ord{symbol: &paca, side: enum.Buy},
			sell(),
			ord{symbol: &paca, side: enum.Buy},
			sell(),
		},
	}, 0)

	// for pending orders, there is no execution order yet
	// so we have to assume the worst-case execution order
	s.assertPDT(tradeSeq{
		filled: []ord{
			buy(),
		},
		pending: []ord{
			buy(),
			sell(), //dt
		},
		incoming: &ord{side: enum.Sell}, //dt
	}, 2)

	s.assertPDT(tradeSeq{
		pending: []ord{
			buy(),
			buy(),
			sell(),
		},
	}, 1)

	s.assertPDT(tradeSeq{
		pending: []ord{
			buy(),
			sell(),
			sell(),
		},
	}, 1)

	s.assertPDT(tradeSeq{
		filled: []ord{
			buy(),
		},
		pending: []ord{
			buy(),
			sell(),
			sell(),
		},
	}, 2)

	// 2 day trades older than a week
	s.assertPDT(tradeSeq{
		filled: []ord{
			ord{tOffset: &old, side: enum.Buy},
			ord{tOffset: &old, side: enum.Sell},
			ord{tOffset: &old, side: enum.Buy},
			ord{tOffset: &old, side: enum.Sell},
			ord{tOffset: &old, side: enum.Buy},
		},
		incoming: &ord{side: enum.Sell},
	}, 0)

	// Selling when the last buy was yesterday
	s.assertPDT(tradeSeq{
		filled: []ord{
			ord{tOffset: &yesterday, side: enum.Buy},
		},
		incoming: &ord{side: enum.Sell},
	}, 0)
}

func (s *AccountSuite) assertPDT(sq tradeSeq, expected int) {
	now := time.Date(2018, 5, 3, 15, 0, 0, 0, calendar.NY)
	tx := db.Begin()

	// create all the filled orders
	for i, filled := range sq.filled {
		filledAt := now.Add(-5 * time.Hour).Add(time.Duration(i+1) * time.Minute).In(calendar.NY)
		if filled.tOffset != nil {
			filledAt = filledAt.Add(*filled.tOffset)
		}
		symbol := s.asset.Symbol
		if filled.symbol != nil {
			symbol = *filled.symbol
		}
		s.genExecution(tx, filled.side, filledAt, symbol)
	}

	// create all the pending orders
	for _, pending := range sq.pending {
		symbol := s.asset.Symbol
		if pending.symbol != nil {
			symbol = *pending.symbol
		}
		s.genPendingOrder(tx, pending.side, symbol)
	}

	// set up the incoming order
	var order *models.Order
	if sq.incoming != nil {
		symbol := s.asset.Symbol
		if sq.incoming.symbol != nil {
			symbol = *sq.incoming.symbol
		}
		order = &models.Order{
			Account:     *s.account.ApexAccount,
			Qty:         decimal.NewFromFloat(100),
			Status:      enum.OrderNew,
			AssetID:     s.asset.ID,
			Symbol:      symbol,
			Type:        enum.Market,
			Side:        sq.incoming.side,
			TimeInForce: enum.GTC,
		}
	}

	pdts, err := PatternDayTrades(tx, s.account, order, now)
	assert.Nil(s.T(), err)
	assert.Equal(s.T(), expected, pdts)
	require.Nil(s.T(), tx.Commit().Error)

	clearOrders(s.T(), db.Begin())
}

func clearOrders(t *testing.T, tx *gorm.DB) {
	tx.Exec("TRUNCATE TABLE orders")
	tx.Exec("TRUNCATE TABLE executions")
	require.Nil(t, tx.Commit().Error)
}

func (s *AccountSuite) genExecution(
	tx *gorm.DB,
	side enum.Side,
	filledAt time.Time,
	symbol string,
) *models.Order {
	order := &models.Order{
		Account:     *s.account.ApexAccount,
		Qty:         decimal.NewFromFloat(100),
		AssetID:     s.asset.ID,
		Symbol:      symbol,
		Type:        enum.Market,
		Side:        side,
		TimeInForce: enum.GTC,
		FilledAt:    &filledAt,
		Status:      enum.OrderFilled,
	}
	if err := tx.Create(order).Error; err != nil {
		assert.FailNow(s.T(), err.Error())
	}
	hundo := decimal.NewFromFloat(100)
	exec := &models.Execution{
		Account:         *s.account.ApexAccount,
		Type:            enum.ExecutionFill,
		Symbol:          symbol,
		Side:            side,
		OrderID:         order.ID,
		OrderType:       order.Type,
		OrderStatus:     order.Status,
		Price:           &hundo,
		Qty:             &hundo,
		AvgPrice:        &hundo,
		LeavesQty:       &hundo,
		CumQty:          &hundo,
		TransactionTime: filledAt,
	}
	if err := tx.Create(exec).Error; err != nil {
		assert.FailNow(s.T(), err.Error())
	}
	return order
}

