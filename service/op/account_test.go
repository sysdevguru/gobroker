func genPositions(tx *gorm.DB, account string, positions []testPosition) (p []*models.Position) {
	for _, pos := range positions {
		id, _ := uuid.NewV4()
		pModel := &models.Position{
			AssetID:        pos.assetID,
			AccountID:      account,
			Qty:            pos.qty,
			Status:         pos.status,
			EntryTimestamp: pos.entryTime,
			EntryPrice:     pos.entryPrice,
			EntryOrderID:   id.String(),
			ExitPrice:      pos.exitPrice,
			ExitTimestamp:  pos.exitTime,
		}
		if err := tx.Create(pModel).Error; err != nil {
			panic(err)
		}

		p = append(p, pModel)
	}
	return p
}

func removePositions(tx *gorm.DB, positions []*models.Position) {
	for _, pos := range positions {
		if err := tx.Delete(pos).Error; err != nil {
			panic(err)
		}
	}
}

